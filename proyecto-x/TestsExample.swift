import Foundation

class NumberOperations {
    func sumNumbers(_ numberOne: Int, _ numberTwo: Int) -> Int {
        return numberOne + numberTwo
    }
    
    func subtractNumbers(_ numberOne: Int, _ numberTwo: Int) -> Int {
        return numberOne - numberTwo
    }
}
